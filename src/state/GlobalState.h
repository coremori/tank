#ifndef GLOBALSTATE_H
#define GLOBALSTATE_H

#include <string>

class GlobalState
{
public:
    GlobalState();

    ///////////////////////////////////////////////////////////////////////
    /// \brief getScore
    /// \return Current Score
    /////////////////////////////////////////////////////////////////////////
    int getScore() const;

    ///////////////////////////////////////////////////////////////////////
    /// \brief getIdCurrentPlayer
    /// \return Player's id
    /////////////////////////////////////////////////////////////////////////
    int getIdCurrentPlayer() const;

    ///////////////////////////////////////////////////////////////////////
    /// \brief getNameCurrentPlayer
    /// \return Player name
    /////////////////////////////////////////////////////////////////////////
    std::string getNameCurrentPlayer() const;

    ///////////////////////////////////////////////////////////////////////
    /// \brief setScore
    /// \param newScore : score
    /// \return void
    /////////////////////////////////////////////////////////////////////////
    void setScore(int newScore);

    ///////////////////////////////////////////////////////////////////////
    /// \brief setIdCurrentPlayer
    /// \param id : new player id
    /// \return void
    /////////////////////////////////////////////////////////////////////////
    void setIdCurrentPlayer(int id);

    ///////////////////////////////////////////////////////////////////////
    /// \brief setNameCurrentPlayer
    /// \param name : name of the player
    /// \return void
    /////////////////////////////////////////////////////////////////////////
    void setNameCurrentPlayer(std::string name);

private :
    int f_score;
    int f_idCurrentPlayer;
    std::string f_nameCurrentPlayer;
};

#endif // GLOBALSTATE_H
